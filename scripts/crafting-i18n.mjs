function getHandlebarsInstance() {
  return globalThis.Handlebars
    ?? foundry.applications?.handlebars
    ?? foundry.applications?.Handlebars
    ?? foundry.templates?.handlebars
    ?? null;
}

export function registerTemplateHelpers() {
  const handlebars = getHandlebarsInstance();
  if (!handlebars?.registerHelper) return false;
  if (!handlebars.helpers?.ct) {
    handlebars.registerHelper("ct", (key, options) => ct(key, options?.hash ?? {}));
  }
  return true;
}

export function ct(key, data = {}) {
  const activeGame = typeof game !== "undefined" ? game : null;
  const configured = activeGame?.settings?.settings?.has(`${MODULE_ID}.moduleLanguage`)
    ? String(activeGame.settings.get(MODULE_ID, "moduleLanguage") || "auto")
    : "auto";
  const foundryLanguage = String(activeGame?.i18n?.lang ?? globalThis.document?.documentElement?.lang ?? "en")
    .toLowerCase()
    .split(/[-_]/)[0];
  const requestedLanguage = configured === "auto" ? foundryLanguage : configured;
  const lang = CT_TRANSLATIONS[requestedLanguage] ? requestedLanguage : "en";
  let text = CT_TRANSLATIONS[lang]?.[key] ?? CT_TRANSLATIONS.en[key] ?? key;
  for (const [token, value] of Object.entries(data ?? {})) {
    text = text.replaceAll(`{${token}}`, String(value ?? ""));
  }
  return text;
}

